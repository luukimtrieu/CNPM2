package com.example.cnpm;

public class CustomListViewEmployee {
    private String name;
    private String work_email;

    public CustomListViewEmployee(String name, String work_email) {
        this.name = name;
        this.work_email = work_email;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getWork_email() {
        return work_email;
    }
    public void setWork_email(String money) {
        this.work_email = work_email;
    }
}
